public class StrUtil {
    public String small(String str) {
        return str.toLowerCase();
    }
    public String big(String str) {
        return str.toUpperCase();
    }
    public int length(String str) {
        return str.length();
    }
    public String unite(String s1, String s2) {
        return (s1 + s2);
    }
}
